    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        choser.showOpenDialog(null);
        File f = choser.getSelectedFile();
        filename = f.getName();
        try {
            Import imp = new Import(f.getAbsolutePath());
            imp.readInstance();
            DefaultTableModel model =   (DefaultTableModel)instances.getModel();
            model.setColumnIdentifiers(new String[]{"Tableau d'instances"});
       
        model.addRow(new Object[]{filename});
        } catch (ReaderException ex) {
            Logger.getLogger(Instances.class.getName()).log(Level.SEVERE, null, ex);
               JOptionPane.showMessageDialog(this, " Assurez-vous qu'il s'agit bien d'un fichier au format txt ","Erreur",JOptionPane.ERROR_MESSAGE);
        }

         
        
    }//GEN-LAST:event_jButton3ActionPerformed

